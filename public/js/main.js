var slide = ["slide1.jpg","slide2.jpg","slide3.jpg"];
var numero = 0;

function ChangeSlide(sens) {
    numero = numero + sens;
    if (numero < 0)
        numero = slide.length - 1;
    if (numero > slide.length - 1)
        numero = 0;
    document.getElementById("slide").src = "img/" + slide[numero];
}